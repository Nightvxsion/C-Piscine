#include <unistd.h>

void	ft_print_comb2(void)
{
	char	x[5];
	int		a;
	int		b;

	x[2] = ' ';
	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			x[0] = (a / 10) + '0';
			x[1] = (a % 10) + '0';
			x[3] = (b / 10) + '0';
			x[4] = (b % 10) + '0';
			write(1, x, 5);
			if (a != 98 || b != 99)
			{
				write(1, ", ", 5);
			}
			b++;
		}
		a++;
	}
}

